);

CREATE TABLE MARCAS_MODELO
(
    ID_MARCA NUMBER GENERATED ALWAYS AS IDENTITY,
    NOMBRE   VARCHAR(30) NOT NULL,
    CONSTRAINT PK_MARCASMODELOS PRIMARY KEY (ID_MARCA)
