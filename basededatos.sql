CREATE TABLE PERFILES_PERMISOS
(
    ID_PERFIL  NUMBER,
    ID_PERMISO NUMBER NOT NULL,
    CONSTRAINT PK_PERFILESPERMISOS PRIMARY KEY (ID_PERFIL, ID_PERMISO)
